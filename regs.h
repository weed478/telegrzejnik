#ifndef MYKERNEL_REGS_H
#define MYKERNEL_REGS_H

#ifdef BCM2836
#define PERIPHERAL_BASE (0x3f000000)
#endif

#ifdef BCM2711
#define PERIPHERAL_BASE (0xfe000000)
#endif

// GPIO

#define GPIO_BASE (PERIPHERAL_BASE + 0x200000)

#define GPIO_GPFSEL0 (GPIO_BASE + 0x00)
#define GPIO_GPFSEL1 (GPIO_BASE + 0x04)
#define GPIO_GPFSEL2 (GPIO_BASE + 0x08)
#define GPIO_GPFSEL3 (GPIO_BASE + 0x0c)
#define GPIO_GPFSEL4 (GPIO_BASE + 0x10)
#define GPIO_GPFSEL5 (GPIO_BASE + 0x14)

#define GPIO_GPSET0 (GPIO_BASE + 0x1c)
#define GPIO_GPSET1 (GPIO_BASE + 0x20)

#define GPIO_GPCLR0 (GPIO_BASE + 0x28)
#define GPIO_GPCLR1 (GPIO_BASE + 0x2c)

#define GPIO_GPLEV0 (GPIO_BASE + 0x34)
#define GPIO_GPLEV1 (GPIO_BASE + 0x38)

#define GPIO_PUP_PDN_CNTRL_REG0 (GPIO_BASE + 0xe4)
#define GPIO_PUP_PDN_CNTRL_REG1 (GPIO_BASE + 0xe8)
#define GPIO_PUP_PDN_CNTRL_REG2 (GPIO_BASE + 0xec)
#define GPIO_PUP_PDN_CNTRL_REG3 (GPIO_BASE + 0xf0)

// System Timer

#define SYSTEM_TIMER_BASE (PERIPHERAL_BASE + 0x003000)

#define SYSTEM_TIMER_CS  (SYSTEM_TIMER_BASE + 0x00)
#define SYSTEM_TIMER_CLO (SYSTEM_TIMER_BASE + 0x04)
#define SYSTEM_TIMER_CHI (SYSTEM_TIMER_BASE + 0x08)

// PWM0

#define PWM0_BASE (PERIPHERAL_BASE + 0x20c000)

#define PWM0_CTL  (PWM0_BASE + 0x00)
#define PWM0_STA  (PWM0_BASE + 0x04)
#define PWM0_DMAC (PWM0_BASE + 0x08)
#define PWM0_RNG1 (PWM0_BASE + 0x10)
#define PWM0_DAT1 (PWM0_BASE + 0x14)
#define PWM0_FIF1 (PWM0_BASE + 0x18)
#define PWM0_RNG2 (PWM0_BASE + 0x20)
#define PWM0_DAT2 (PWM0_BASE + 0x24)

// PWM1

#define PWM1_BASE (PERIPHERAL_BASE + 0x20c800)

#define PWM1_CTL  (PWM1_BASE + 0x00)
#define PWM1_STA  (PWM1_BASE + 0x04)
#define PWM1_DMAC (PWM1_BASE + 0x08)
#define PWM1_RNG1 (PWM1_BASE + 0x10)
#define PWM1_DAT1 (PWM1_BASE + 0x14)
#define PWM1_FIF1 (PWM1_BASE + 0x18)
#define PWM1_RNG2 (PWM1_BASE + 0x20)
#define PWM1_DAT2 (PWM1_BASE + 0x24)

// CM

#define CM_BASE (PERIPHERAL_BASE + 0x101000)

#define CM_PWMCTL (CM_BASE + 0xa0)
#define CM_PWMDIV (CM_BASE + 0xa4)

#endif
